# scMultiPert — Module 5: Multi-modal Fine-tuning via QLoRA (v2.4)
# =======================================================================
# This module executes the dual-stage alignment of BioMistral-7B. It 
# leverages Quantized Low-Rank Adaptation (QLora) to integrate biological 
# soft tokens into the LLM's latent space while maintaining a low memory 
# footprint.
#
# Architectural Features:
#   - Dynamic Latent Injection: The 'SoftTokenCollator' performs on-the-fly 
#     splicing of aligned biological embeddings into the input sequence.
#   - Context Budgeting: Implements strict (2048 - 2) sequence management 
#     to accommodate soft tokens without exceeding the LLM's window.
#   - Information Safeguards: Employs 'Neutral Priors' derived exclusively 
#     from training split keys to ensure zero-leakage evaluation.
#   - Telemetry Sync: Full integration with Weights & Biases (WandB) for 
#     tracking both LoRA and Aligner parameter updates.
#
# Dependencies: transformers, peft, trl, bitsandbytes, torch, datasets, wandb
# =======================================================================

