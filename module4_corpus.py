def serialize_corpus(
    examples: List[ChatMLExample],
    output_dir: Path,
    centroids: Dict[str, torch.Tensor]
) -> Tuple[str, str]:
    """
    Write training corpus to JSONL and centroids to PyTorch format.
    
    Outputs:
      - train_corpus.jsonl: Training examples with split='train'
      - test_corpus.jsonl: Test examples with split='test'
      - scrna_centroids.pt: Tensor dictionary for Module 5/6 retrieval
    
    Args:
        examples: List of ChatMLExample objects with assigned splits.
        output_dir: Path object for output directory.
        centroids: Dictionary of perturbation centroids for Stage 2 training.
    
    Returns:
        Tuple of (train_path, test_path) as strings.
    
    Raises:
        CorpusBuilderError: If output directory cannot be created or is not writable.
    """
    try:
        output_dir.mkdir(parents=True, exist_ok=True)
    except OSError as e:
        raise CorpusBuilderError(
            f"Cannot create output directory {output_dir}: {e}"
        )
    
    # Split examples
    train_examples = [ex for ex in examples if ex.split == "train"]
    test_examples = [ex for ex in examples if ex.split == "test"]
    
    # Serialize to JSONL
    train_path = output_dir / "train_corpus.jsonl"
    test_path = output_dir / "test_corpus.jsonl"
    
    with open(train_path, "w", encoding="utf-8") as f:
        for ex in train_examples:
            f.write(json.dumps(ex.to_dict(), ensure_ascii=False) + "\n")
    
    with open(test_path, "w", encoding="utf-8") as f:
        for ex in test_examples:
            f.write(json.dumps(ex.to_dict(), ensure_ascii=False) + "\n")
    
    # Save centroids for Module 5 soft token retrieval
    centroids_path = output_dir / "scrna_centroids.pt"
    torch.save(centroids, centroids_path)
    
    print(f"[Module 4] Serialized corpus:")
    print(f"  Train: {train_path} ({len(train_examples)} examples)")
    print(f"  Test:  {test_path} ({len(test_examples)} examples)")
    print(f"  Centroids: {centroids_path} ({len(centroids)} perturbations)")
    
    return str(train_path), str(test_path)


