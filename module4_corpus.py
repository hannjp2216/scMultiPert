# SECTION 6 — Gene Vocabulary Extraction
# ---------------------------------------------------------------------------

def extract_gene_vocabulary(modalities: Dict[str, Any]) -> Set[str]:
    """
    Compile comprehensive gene vocabulary from all data modalities.
    
    Aggregates gene symbols from:
      - MAVE: 'gene_symbol' column
      - CRISPR: 'gene' column  
      - scRNA: 'perturbed_genes' column (handles list format and combinatorial)
    
    This vocabulary drives the gene-based splitting strategy to prevent leakage.
    
    Args:
        modalities: Dictionary from Module 1 with 'scrna', 'crispr', 'mave' keys.
    
    Returns:
        Set of all unique gene symbols across modalities.
    
    Raises:
        CorpusBuilderError: If any modality is missing required gene columns.
    """
    all_genes = set()
    
    # MAVE genes
    if "gene_symbol" in modalities["mave"].columns:
        all_genes.update(modalities["mave"]["gene_symbol"].unique())
    else:
        raise CorpusBuilderError(
            "MAVE DataFrame missing 'gene_symbol' column. "
            "Check Module 1 load_multi_mave() output."
        )
    
    # CRISPR genes
    if "gene" in modalities["crispr"].columns:
        all_genes.update(modalities["crispr"]["gene"].unique())
    else:
        raise CorpusBuilderError(
            "CRISPR DataFrame missing 'gene' column. "
            "Check Module 1 load_depmap_crispr() output."
        )
    
    # scRNA genes (handles list format in perturbed_genes)
    if "perturbed_genes" in modalities["scrna"].obs.columns:
        for pert_genes in modalities["scrna"].obs["perturbed_genes"]:
            if isinstance(pert_genes, list):
                all_genes.update(pert_genes)
            elif isinstance(pert_genes, str):
                # Handle string representation if needed
                all_genes.update(_extract_gene_components(pert_genes))
    else:
        raise CorpusBuilderError(
            "scRNA AnnData missing 'perturbed_genes' column. "
            "Check Module 1 load_norman2019() output."
        )
    
    # Remove None/empty entries
    all_genes = {g for g in all_genes if g and isinstance(g, str)}
    
    print(f"[Module 4] Extracted gene vocabulary: {len(all_genes)} unique genes")
    
    return all_genes


