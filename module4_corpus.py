"""
scMultiPert — Module 4: Corpus Builder
============================================
Builds the instruction-tuning dataset for Stage 1 (text-only) and Stage 2 
(multi-modal soft token) training of the scMultiPert biomedical assistant.

This module consumes embeddings and metadata from Module 2 (Encoders) and 
structures them into ChatML-formatted training examples. It implements 
gene-based data splitting to prevent information leakage between training 
and test sets, critical for evaluating model generalization to unseen genes.

Architecture Integration:
  - Module 1 (Preprocessing): Provides standardized AnnData and DataFrames.
  - Module 2 (Encoders): Provides cell_embeddings [N, 256], var_embeddings [M, 320],
    var_texts, var_ids, and crispr_texts.
  - Module 3 (Alignment): Consumes centroids computed here for Stage 2 training.
  - Module 5 (Training): Consumes the train_corpus.jsonl and test_corpus.jsonl 
    generated by this module.
  - Validation: Compatible with validate_pipeline.py checks for gene leakage,
    template diversity, and bio-context token presence.

Data Flow:
  1. Compute perturbation centroids from cell embeddings (mean pooling per perturbation).
  2. Build modality-specific ChatML examples (MAVE, scRNA, CRISPR).
  3. Extract dynamic gene vocabulary from all modalities.
  4. Apply gene-based split (no gene overlap between train/test).
  5. Serialize to JSONL with metadata for downstream consumption.

Dependencies: torch, pandas, anndata, numpy, json, random, pathlib
"""

import json
import random
import torch
import numpy as np
import pandas as pd
import anndata as ad
from pathlib import Path
from dataclasses import dataclass, asdict
from typing import Optional, List, Dict, Set, Tuple, Any

# ---------------------------------------------------------------------------
# SECTION 0 — Custom Exceptions
# ---------------------------------------------------------------------------

class Module4Error(Exception):
    """Base class for Module 4 domain-specific errors."""
    pass


class SplitError(Module4Error):
    """Raised when gene-based data splitting encounters invalid configuration."""
    pass


class AlignmentError(Module4Error):
    """Raised when data misalignment detected between M1/M2/M3 outputs."""
    pass


class CorpusBuilderError(Module4Error):
    """Raised when corpus construction fails due to data quality issues."""
    pass


# ---------------------------------------------------------------------------
# SECTION 1 — Constants & Templates
# ---------------------------------------------------------------------------

SYSTEM_PROMPT = (
    "You are scMultiPert, a perturbation-aware biomedical assistant "
    "specialized in predicting and explaining the effects of genetic "
    "perturbations on cellular state and protein function."
)

# Token marking injection point for soft embeddings in Stage 2
BIO_CONTEXT_TOKEN = "<|BIO_CONTEXT|>"

# Expected embedding dimensions (from Module 2 encoders)
GENEFORMER_DIM = 256
ESM2_8M_DIM = 320

# Question templates for MAVE (variant effect) modality
MAVE_QUESTION_TEMPLATES = [
    "What is the functional effect of {gene_symbol} variant {variant_id}?",
    "How does the {aa_ref}{position}{aa_alt} substitution in {gene_symbol} affect function?",
    "What does the SGE score {score} tell us about {variant_id}?",
    "Predict the functional impact of {gene_symbol} variant {variant_id} based on its SGE score of {score}.",
    "Is {gene_symbol} variant {variant_id} likely to be pathogenic?",
]

# Question templates for scRNA-seq (transcriptional response) modality
SCRNA_QUESTION_TEMPLATES = [
    "What is the transcriptional response to {perturbation} in K562 cells?",
    "Describe the gene expression changes following {perturbation} perturbation.",
    "Predict the molecular phenotype of cells with {perturbation} activation.",
    "How does {perturbation} perturbation alter the transcriptome of K562 cells?",
    "Identify the marker genes characterizing the {perturbation} perturbation state.",
]

# Question templates for CRISPR (gene essentiality) modality
CRISPR_QUESTION_TEMPLATES = [
    "Is {gene} essential in {lineage} cell lines?",
    "What is the fitness effect of knocking out {gene} in {lineage} tissue?",
    "Does loss of {gene} function impair viability in {lineage} cancer models?",
    "Predict the essentiality of {gene} based on CRISPR screening data in {lineage}.",
    "What percentage of {lineage} cell lines show dependency on {gene}?",
]


