def build_corpus(
    modalities: Dict[str, Any],
    m2_outputs: Dict[str, Any],
    output_dir: str = "corpus/",
    test_fraction: float = 0.15,
    seed: int = 42
) -> Dict[str, Any]:
    """
    Main execution flow for corpus construction.
    
    Orchestrates the complete pipeline from embeddings to serialized training
    data, ensuring compatibility with all upstream modules and validation
    requirements.
    
    Integration Points:
      - Module 1: Consumes 'scrna' (AnnData), 'crispr' (DataFrame), 'mave' (DataFrame).
      - Module 2: Consumes cell_embeddings, cell_metadata, var_embeddings, 
                 var_texts, var_ids, crispr_texts, crispr_agg.
      - Module 3: Provides centroids for potential cross-modal validation.
      - Validation: Outputs compatible with validate_pipeline.py checks.
    
    Args:
        modalities: Dictionary from Module 1 load_all_modalities().
        m2_outputs: Dictionary from Module 2 encode_all_modalities().
        output_dir: Directory path for output files.
        test_fraction: Fraction of genes to hold out for testing.
        seed: Random seed for reproducibility.
    
    Returns:
        Dictionary containing:
          - train_path: Path to train_corpus.jsonl
          - test_path: Path to test_corpus.jsonl
          - centroids: Dictionary of perturbation centroids
          - gene_vocabulary: Set of all genes used for splitting
          - n_examples: Total number of examples generated
          - modality_counts: Dict with counts per modality
    
    Raises:
        Module4Error: If any stage of corpus construction fails.
    """
    print("=" * 60)
    print("scMultiPert | Module 4: Corpus Builder")
    print("=" * 60)
    
    # Stage 1: Compute perturbation centroids for Stage 2 training
    print("\n[Stage 1/5] Computing perturbation centroids...")
    try:
        centroids = compute_perturbation_centroids(
            m2_outputs["cell_embeddings"],
            m2_outputs["cell_metadata"]
        )
        print(f"  Computed {len(centroids)} perturbation centroids")
    except Exception as e:
        raise CorpusBuilderError(f"Centroid computation failed: {e}") from e
    
    # Stage 2: Build modality-specific examples
    print("\n[Stage 2/5] Building MAVE examples...")
    try:
        mave_examples = build_mave_examples(
            modalities["mave"],
            m2_outputs["var_texts"],
            m2_outputs["var_ids"],
            seed=seed
        )
        print(f"  Generated {len(mave_examples)} MAVE examples")
    except Exception as e:
        raise CorpusBuilderError(f"MAVE example building failed: {e}") from e
    
    print("\n[Stage 3/5] Building scRNA-seq examples...")
    try:
        scrna_examples = build_scrna_examples(
            modalities["scrna"],
            centroids,
            seed=seed
        )
        print(f"  Generated {len(scrna_examples)} scRNA examples")
    except Exception as e:
        raise CorpusBuilderError(f"scRNA example building failed: {e}") from e
    
    print("\n[Stage 4/5] Building CRISPR examples...")
    try:
        crispr_examples = build_crispr_examples(
            m2_outputs["crispr_texts"],
            m2_outputs["crispr_agg"],
            seed=seed
        )
        print(f"  Generated {len(crispr_examples)} CRISPR examples")
    except Exception as e:
        raise CorpusBuilderError(f"CRISPR example building failed: {e}") from e
    
    # Combine all examples
    all_examples = mave_examples + scrna_examples + crispr_examples
    
    modality_counts = {
        "mave": len(mave_examples),
        "scrna": len(scrna_examples),
        "crispr": len(crispr_examples),
        "total": len(all_examples)
    }
    print(f"\n[Summary] Total examples: {len(all_examples)}")
    print(f"  MAVE:   {len(mave_examples)}")
    print(f"  scRNA:  {len(scrna_examples)}")
    print(f"  CRISPR: {len(crispr_examples)}")
    
    # Stage 3: Extract gene vocabulary for splitting
    print("\n[Stage 5/5] Extracting gene vocabulary and applying split...")
    try:
        gene_vocab = extract_gene_vocabulary(modalities)
    except Exception as e:
        raise CorpusBuilderError(f"Gene vocabulary extraction failed: {e}") from e
    
    # Stage 4: Apply gene-based split
    try:
        all_examples = split_by_gene(
            all_examples,
            gene_vocab,
            test_fraction=test_fraction,
            seed=seed
        )
    except Exception as e:
        raise SplitError(f"Gene-based splitting failed: {e}") from e
    
    # Stage 5: Serialize outputs
    print("\n[Serialization] Writing corpus files...")
    out_path = Path(output_dir)
    try:
        train_path, test_path = serialize_corpus(all_examples, out_path, centroids)
    except Exception as e:
        raise CorpusBuilderError(f"Serialization failed: {e}") from e
    
    print("\n" + "=" * 60)
    print("Module 4 Complete")
    print("=" * 60)
    
    return {
        "train_path": train_path,
        "test_path": test_path,
        "centroids": centroids,
        "gene_vocabulary": gene_vocab,
        "n_examples": len(all_examples),
        "modality_counts": modality_counts
    }


