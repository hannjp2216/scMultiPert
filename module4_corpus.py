# SECTION 3 — Centroid Computation
# ---------------------------------------------------------------------------

def compute_perturbation_centroids(
    cell_embeddings: torch.Tensor, 
    cell_metadata: pd.DataFrame
) -> Dict[str, torch.Tensor]:
    """
    Compute mean embedding vectors for each unique perturbation condition.
    
    Aggregates single-cell embeddings by perturbation label to create 
    representative vectors for each experimental condition. Control cells
    are excluded from centroid computation as they represent baseline states
    rather than perturbation responses.
    
    Args:
        cell_embeddings: Tensor of shape [N_cells, 256] from GeneformerEncoder.
        cell_metadata: DataFrame with columns 'is_control', 'embedding_label', 
                      and 'embedding_idx' synchronized with cell_embeddings.
    
    Returns:
        Dictionary mapping perturbation labels (e.g., "CDKN1A", "FOXA1+TBX2") 
        to their mean embedding tensors [256].
    
    Raises:
        AlignmentError: If cell_embeddings and cell_metadata have mismatched lengths.
        CorpusBuilderError: If no valid perturbation cells found after filtering controls.
    """
    if len(cell_embeddings) != len(cell_metadata):
        raise AlignmentError(
            f"Cell embeddings ({len(cell_embeddings)}) and metadata ({len(cell_metadata)}) "
            f"have mismatched lengths. Ensure Module 2 outputs are synchronized."
        )
    
    # Filter to perturbation cells only (exclude controls)
    pert_mask = ~cell_metadata["is_control"]
    pert_metadata = cell_metadata[pert_mask].copy()
    
    if len(pert_metadata) == 0:
        raise CorpusBuilderError(
            "No perturbation cells found after filtering controls. "
            "Check 'is_control' labels in Module 1 output."
        )
    
    centroids = {}
    
    # Group by embedding_label (handles both single and combinatorial perturbations)
    for label, group in pert_metadata.groupby("embedding_label"):
        indices = group["embedding_idx"].tolist()
        # Mean pooling across cells for this perturbation
        centroid = cell_embeddings[indices].mean(dim=0).cpu()
        centroids[label] = centroid
    
    return centroids


