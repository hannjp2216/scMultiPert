def validate_corpus_integrity(corpus_paths: Dict[str, str]) -> Dict[str, Any]:
    """
    Validate corpus files for pipeline integrity checks.
    
    Helper function for validate_pipeline.py to perform M4 checks without
    reloading the full build_corpus logic. Validates:
      - Gene leakage between splits (CriticalLeakageError if detected)
      - Template diversity per modality
      - BIO_CONTEXT_TOKEN presence in bio-context examples
    
    Args:
        corpus_paths: Dictionary with 'train_path' and 'test_path' keys.
    
    Returns:
        Validation report dictionary with check results.
    
    Raises:
        CriticalLeakageError: If gene overlap detected between train/test.
        CorpusBuilderError: If files cannot be read.
    """
    from collections import Counter
    
    def load_jsonl(path: str) -> List[Dict]:
        examples = []
        try:
            with open(path, 'r', encoding='utf-8') as f:
                for line in f:
                    examples.append(json.loads(line.strip()))
        except Exception as e:
            raise CorpusBuilderError(f"Cannot read {path}: {e}") from e
        return examples
    
    def extract_template(question: str) -> str:
        """Extract template signature from question text."""
        # Simple heuristic: first 20 chars + last 20 chars
        question = question.replace(BIO_CONTEXT_TOKEN, "").strip()
        if len(question) <= 40:
            return question
        return question[:20] + "..." + question[-20:]
    
    train_ex = load_jsonl(corpus_paths["train_path"])
    test_ex = load_jsonl(corpus_paths["test_path"])
    
    results = {}
    
    # Check 1: Gene leakage (CRITICAL)
    train_genes = set()
    test_genes = set()
    
    for ex in train_ex:
        if ex.get("gene_id"):
            train_genes.update(_extract_gene_components(ex["gene_id"]))
    
    for ex in test_ex:
        if ex.get("gene_id"):
            test_genes.update(_extract_gene_components(ex["gene_id"]))
    
    intersection = train_genes & test_genes
    if intersection:
        raise CriticalLeakageError(
            f"Data leakage detected: {len(intersection)} genes appear in both "
            f"train and test splits: {list(intersection)[:10]}..."
        )
    
    results["leakage_pass"] = True
    results["n_train_genes"] = len(train_genes)
    results["n_test_genes"] = len(test_genes)
    
    # Check 2: Template diversity
    for modality in ["mave", "scrna", "crispr"]:
        modality_examples = [ex for ex in train_ex if ex.get("modality") == modality]
        if not modality_examples:
            continue
        
        # Extract user message (index 1) content
        templates = []
        for ex in modality_examples:
            messages = ex.get("messages", [])
            if len(messages) > 1:
                user_content = messages[1].get("content", "")
                templates.append(extract_template(user_content))
        
        if templates:
            counter = Counter(templates)
            max_freq = max(counter.values()) / len(templates)
            results[f"template_diversity_{modality}"] = max_freq
            results[f"template_pass_{modality}"] = max_freq <= 0.60
    
    # Check 3: BIO_CONTEXT_TOKEN presence
    bio_examples = [ex for ex in train_ex if ex.get("has_bio_context")]
    if bio_examples:
        with_token = sum(
            1 for ex in bio_examples 
            if BIO_CONTEXT_TOKEN in ex.get("messages", [{}])[1].get("content", "")
        )
        coverage = (with_token / len(bio_examples)) * 100
        results["bio_token_coverage_pct"] = coverage
        results["bio_token_pass"] = coverage == 100.0
    
    # Overall pass
    pass_keys = [k for k in results if k.endswith("_pass")]
    results["pass"] = all(results[k] for k in pass_keys)
    
    return results


