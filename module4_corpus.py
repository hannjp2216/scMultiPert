# SECTION 5 — Gene-Based Data Splitting
# ---------------------------------------------------------------------------

def _extract_gene_components(gene_id: Optional[str]) -> Set[str]:
    """
    Parse gene identifier into constituent gene symbols.
    
    Handles both single genes ("BRCA1") and combinatorial perturbations 
    ("CDKN1A+FOXA1") by splitting on '+' delimiter and stripping whitespace.
    
    Args:
        gene_id: Gene identifier string or None.
    
    Returns:
        Set of individual gene symbols. Empty set if gene_id is None.
    """
    if gene_id is None:
        return set()
    
    # Handle combinatorial format: "GeneA+GeneB" -> {"GeneA", "GeneB"}
    components = {g.strip() for g in gene_id.split("+")}
    return components


def split_by_gene(
    examples: List[ChatMLExample],
    all_gene_symbols: Set[str],
    test_fraction: float = 0.15,
    seed: int = 42
) -> List[ChatMLExample]:
    """
    Assign examples to train/test splits ensuring no gene overlap.
    
    Implements strict gene-level holdout to prevent data leakage. For 
    combinatorial perturbations (e.g., "A+B"), the example is assigned to 
    test if ANY component gene is in the test set. This ensures the model
    is evaluated on true zero-shot gene prediction.
    
    Validation compatibility:
      - Checked by validate_pipeline.py check_gene_leakage (CriticalLeakageError 
        if intersection non-empty).
      - Required for M4.leakage_pass validation metric.
    
    Args:
        examples: List of ChatMLExample objects to split.
        all_gene_symbols: Set of all unique genes across all modalities.
        test_fraction: Proportion of genes to hold out for testing (0.0-1.0).
        seed: Random seed for reproducible gene selection.
    
    Returns:
        List of ChatMLExample objects with 'split' attribute assigned.
    
    Raises:
        SplitError: If gene vocabulary is empty or test_fraction invalid.
    """
    if not all_gene_symbols:
        raise SplitError(
            "Gene vocabulary is empty. Ensure Module 1 outputs contain valid "
            "gene symbols in 'gene_symbol' and 'perturbed_genes' columns."
        )
    
    if not (0.0 < test_fraction < 1.0):
        raise SplitError(
            f"Invalid test_fraction: {test_fraction}. Must be between 0.0 and 1.0."
        )
    
    rng = random.Random(seed)
    n_test = max(1, int(len(all_gene_symbols) * test_fraction))
    test_genes = set(rng.sample(sorted(all_gene_symbols), k=n_test))
    
    print(f"[Module 4] Gene-based split: {len(test_genes)} test genes "
          f"({len(all_gene_symbols) - len(test_genes)} train genes)")
    print(f"[Module 4] Test gene sample: {list(test_genes)[:5]}...")
    
    for example in examples:
        if example.gene_id is None:
            # Default to train for examples without gene association
            example.split = "train"
            continue
        
        # Extract component genes (handles combinatorial)
        components = _extract_gene_components(example.gene_id)
        
        # Assign to test if ANY component is in test set (conservative)
        if components.intersection(test_genes):
            example.split = "test"
        else:
            example.split = "train"
    
    # Validation: Report split distribution
    n_train = sum(1 for ex in examples if ex.split == "train")
    n_test = sum(1 for ex in examples if ex.split == "test")
    print(f"[Module 4] Example distribution: {n_train} train, {n_test} test")
    
    return examples


