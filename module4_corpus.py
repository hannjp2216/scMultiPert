def build_mave_examples(
    mave_df: pd.DataFrame, 
    var_texts: List[str], 
    var_ids: List[str], 
    seed: int = 42
) -> List[ChatMLExample]:
    """
    Construct ChatML examples from MAVE variant functional data.
    
    Creates instruction-following examples for protein variant effect prediction.
    Each example pairs a randomly selected question template with structured
    variant metadata and the pre-generated text description from Module 2.
    
    Args:
        mave_df: DataFrame from Module 1 with variant annotations including
                'variant_id', 'gene_symbol', 'aa_ref', 'aa_alt', 'position', 
                'sge_score', 'function_class'.
        var_texts: List of structured text descriptions from ESMVariantEncoder.
        var_ids: List of variant identifiers corresponding to var_texts.
        seed: Random seed for reproducible template selection.
    
    Returns:
        List of ChatMLExample objects for MAVE modality.
    
    Raises:
        AlignmentError: If var_texts and var_ids have mismatched lengths.
        CorpusBuilderError: If no valid variants found in lookup mapping.
    """
    if len(var_texts) != len(var_ids):
        raise AlignmentError(
            f"MAVE text/ID misalignment: {len(var_texts)} texts vs {len(var_ids)} IDs. "
            f"Re-run Module 2 ESMVariantEncoder to regenerate synchronized outputs."
        )
    
    # Build lookup for efficient variant retrieval
    mave_lookup = {row["variant_id"]: row for _, row in mave_df.iterrows()}
    examples = []
    rng = random.Random(seed)
    
    for vid, text in zip(var_ids, var_texts):
        if vid not in mave_lookup:
            continue  # Skip variants not in current gene set
        
        row = mave_lookup[vid]
        
        # Format question with random template
        question = rng.choice(MAVE_QUESTION_TEMPLATES).format(
            gene_symbol=row["gene_symbol"],
            variant_id=vid,
            aa_ref=row["aa_ref"],
            aa_alt=row["aa_alt"],
            position=int(row["position"]),
            score=f"{row['sge_score']:.2f}"
        )
        
        # Construct ChatML message sequence with bio-context token for Stage 2
        example = ChatMLExample(
            messages=[
                {"role": "system", "content": SYSTEM_PROMPT},
                {"role": "user", "content": f"{BIO_CONTEXT_TOKEN} {question}"},
                {"role": "assistant", "content": text}
            ],
            modality="mave",
            has_bio_context=True,
            gene_id=row["gene_symbol"],
            embedding_key=vid,
            bio_context_meta={
                "expected_cell_dim": GENEFORMER_DIM,
                "expected_var_dim": ESM2_8M_DIM,
                "sge_score": float(row["sge_score"]),
                "function_class": row["function_class"]
            }
        )
        examples.append(example)
    
    if not examples:
        raise CorpusBuilderError(
            "No MAVE examples generated. Check variant_id alignment between "
            "mave_df and var_ids from Module 2."
        )
    
    return examples


def build_scrna_examples(
    adata: ad.AnnData, 
    centroids: Dict[str, torch.Tensor], 
    seed: int = 42
) -> List[ChatMLExample]:
    """
    Construct ChatML examples from scRNA-seq perturbation centroids.
    
    Generates instruction-tuning examples for transcriptional response prediction.
    Each perturbation centroid is described using the top differentially 
    expressed genes as a biological signature.
    
    Args:
        adata: AnnData object from Module 1 with raw counts and perturbation 
               metadata in obs.
        centroids: Dictionary mapping perturbation labels to mean embedding 
                  vectors from compute_perturbation_centroids().
        seed: Random seed for reproducible question generation.
    
    Returns:
        List of ChatMLExample objects for scRNA modality.
    
    Raises:
        CorpusBuilderError: If centroids dictionary is empty.
        AlignmentError: If perturbation labels in centroids not found in adata.
    """
    if not centroids:
        raise CorpusBuilderError(
            "Empty centroids dictionary provided. Run compute_perturbation_centroids() "
            "on valid cell embeddings first."
        )
    
    examples = []
    rng = random.Random(seed)
    
    for pert_label, centroid_emb in centroids.items():
        # Validate perturbation exists in AnnData
        if pert_label not in adata.obs["perturbation"].values:
            raise AlignmentError(
                f"Perturbation label '{pert_label}' in centroids not found in "
                f"AnnData obs. Check Module 1 preprocessing consistency."
            )
        
        # Extract mean expression for this perturbation to generate biological description
        pert_mask = adata.obs["perturbation"] == pert_label
        mean_expr = adata[pert_mask].X.mean(axis=0)
        
        # Handle sparse matrix conversion
        if hasattr(mean_expr, "A1"):
            mean_expr = mean_expr.A1
        
        # Get top 5 marker genes as transcriptional signature
        top_idx = np.argsort(mean_expr)[-5:]
        top_genes = adata.var_names[top_idx].tolist()
        
        # Construct biologically grounded response
        response = (
            f"The perturbation of {pert_label} leads to a distinct transcriptional state "
            f"characterized by high expression of {', '.join(top_genes)}. "
            f"This suggests a significant shift in cellular homeostasis and regulatory "
            f"networks associated with {pert_label} function."
        )
        
        question = rng.choice(SCRNA_QUESTION_TEMPLATES).format(perturbation=pert_label)
        
        example = ChatMLExample(
            messages=[
                {"role": "system", "content": SYSTEM_PROMPT},
                {"role": "user", "content": f"{BIO_CONTEXT_TOKEN} {question}"},
                {"role": "assistant", "content": response}
            ],
            modality="scrna",
            has_bio_context=True,
            gene_id=pert_label,  # Supports "GeneA+GeneB" format
            embedding_key=pert_label,
            bio_context_meta={
                "expected_cell_dim": GENEFORMER_DIM,
                "expected_var_dim": ESM2_8M_DIM,
                "n_cells": int(pert_mask.sum()),
                "top_markers": top_genes
            }
        )
        examples.append(example)
    
    return examples


def build_crispr_examples(
    crispr_texts: List[str], 
    crispr_agg: pd.DataFrame, 
    seed: int = 42
) -> List[ChatMLExample]:
    """
    Construct ChatML examples from CRISPR screening data.
    
    Creates text-only instruction examples for gene essentiality prediction.
    Unlike MAVE and scRNA modalities, CRISPR examples do not use soft token
    injection (has_bio_context=False) as they rely purely on textual descriptions
    of fitness scores and essentiality rates.
    
    Args:
        crispr_texts: List of structured text descriptions from Module 2 
                     encode_crispr_as_text().
        crispr_agg: DataFrame with aggregated CRISPR statistics including 
                   'gene', 'lineage', 'mean_fitness', 'essential_rate'.
        seed: Random seed for reproducible question generation.
    
    Returns:
        List of ChatMLExample objects for CRISPR modality.
    
    Raises:
        AlignmentError: If crispr_texts and crispr_agg have mismatched lengths.
    """
    if len(crispr_texts) != len(crispr_agg):
        raise AlignmentError(
            f"CRISPR text/data misalignment: {len(crispr_texts)} texts vs "
            f"{len(crispr_agg)} aggregated rows. Re-run Module 2 CRISPR encoding."
        )
    
    examples = []
    rng = random.Random(seed)
    
    for i, (_, row) in enumerate(crispr_agg.iterrows()):
        question = rng.choice(CRISPR_QUESTION_TEMPLATES).format(
            gene=row["gene"],
            lineage=row["lineage"]
        )
        
        example = ChatMLExample(
            messages=[
                {"role": "system", "content": SYSTEM_PROMPT},
                {"role": "user", "content": question},  # No bio-context token for text-only
                {"role": "assistant", "content": crispr_texts[i]}
            ],
            modality="crispr",
            has_bio_context=False,  # Pure text modality
            gene_id=row["gene"],
            embedding_key=None,
            bio_context_meta={
                "mean_fitness": float(row["mean_fitness"]),
                "essential_rate": float(row["essential_rate"]),
                "n_cell_lines": int(row["n_cell_lines"])
            }
        )
        examples.append(example)
    
    return examples


