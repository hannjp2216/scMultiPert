@dataclass
class ChatMLExample:
    """
    Structured container for instruction-tuning examples in ChatML format.
    
    Attributes:
        messages: List of dicts with 'role' and 'content' keys following ChatML spec.
        modality: Source modality identifier ('mave', 'scrna', or 'crispr').
        has_bio_context: Whether this example requires soft token injection.
        gene_id: Primary gene identifier (supports combinatorial "A+B" format).
        embedding_key: Key for retrieving pre-computed embeddings (variant_id or pert_label).
        bio_context_meta: Metadata dict with expected dimensions for validation.
        split: Data split assignment ('train' or 'test').
    """
    messages: List[Dict[str, str]]
    modality: str
    has_bio_context: bool = False
    gene_id: Optional[str] = None
    embedding_key: Optional[str] = None
    bio_context_meta: Optional[Dict[str, Any]] = None
    split: str = "train"

    def to_dict(self) -> Dict[str, Any]:
        """Serialize to dictionary for JSONL serialization."""
        return asdict(self)


